SELECT sc.`id`, s.`name`, s.`slug`, s.`sort_order`
FROM `epc_type_seed` s
INNER JOIN `epc_categories` c
  ON c.`slug` = s.`cat_slug` AND c.`is_active` = 1
INNER JOIN `epc_subcategories` sc
  ON sc.`category_id` = c.`id` AND sc.`slug` = s.`sub_slug` AND sc.`is_active` = 1
ON DUPLICATE KEY UPDATE
  `name`       = VALUES(`name`),
  `sort_order` = VALUES(`sort_order`);

