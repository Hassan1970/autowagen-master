-- =====================================================================
-- Autowagen Master — EPC — Multiple Types for **every** subcategory
--
-- Adds ~5 real **Types** (Level 3) per subcategory (Engine through Accessories).
-- Uses a temporary seed table + join on `epc_categories.slug` + `epc_subcategories.slug`.
-- Temp columns use **utf8mb4_unicode_ci** (same as `02_epc.sql`) so MySQL 8 does not throw
-- **#1267 Illegal mix of collations** (utf8mb4_0900_ai_ci vs utf8mb4_unicode_ci) on `slug = slug`.
-- **ON DUPLICATE KEY UPDATE** on (`subcategory_id`,`slug`) → safe re-run.
--
-- **After this file**, run again in order (extends spine for new types):
--   1) `sql/02d_epc_spine_step3.sql`
--   2) `sql/02e_epc_variants_dismantler.sql`  (optional)
--
-- Also compatible with `02f` (doors): duplicate slugs merge cleanly.
-- =====================================================================

DROP TEMPORARY TABLE IF EXISTS `epc_type_seed`;
